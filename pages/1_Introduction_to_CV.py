import streamlit as st

st.title("Introduction to Computer Vision")
st.header("What is Computer Vision?")
st.write("""
**Computer Vision (CV)** is a field of artificial intelligence (AI) that enables computers to interpret and process visual information from the world, similar to how humans use their eyes. The goal of computer vision is to understand and extract meaningful information from digital images or videos and take actions or make decisions based on this understanding.
""")

st.subheader("Applications of Computer Vision")
st.write("""
Computer Vision has a wide range of applications across different industries:
- **Medical Imaging**: Detecting tumors and other abnormalities in radiology images.
- **Autonomous Vehicles**: Detecting objects like vehicles, pedestrians, and traffic signals.
- **Facial Recognition**: Identifying individuals for security purposes.
- **Retail**: Automating checkout processes.
""")

st.subheader("Advantages of Computer Vision")
st.write("""
- **Automates Tedious Tasks**: Automates repetitive tasks like sorting and counting.
- **High Speed**: Processes large amounts of visual data quickly.
- **Enhanced Accuracy**: Provides high accuracy in critical fields like medical diagnostics.
""")

st.subheader("Disadvantages of Computer Vision")
st.write("""
- **High Computational Cost**: Requires significant processing power.
- **Data Dependency**: Needs a large amount of labeled data.
- **Sensitivity to Environment**: Sensitive to changes in lighting and angles.
""")

